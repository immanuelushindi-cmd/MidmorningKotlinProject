fun main() {

    //Arithmetic Operators
    var x =300
    var y = 5

    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y)

    //Comparison Operators
    println(x<y)
    println(x>y)
    println(x<=y)
    println(x==y)
    println(x!=y)

    //Assignment Operators
    var number = 10
    println(number)

    number += 3
    println(number)

    //Logical Operators
    println(400>200 && 50==25)

    println(!(400>200 || 50==25))
}